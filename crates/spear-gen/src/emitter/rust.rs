fn primitive_read_expr_and_size(rust_type: &str, byte_size: usize) -> (String, usize) {
    let expr = match (rust_type, byte_size) {
        ("bool", 1) => "_codec::read_bool(buf, offset)".to_owned(),
        ("i32", 4) => "_codec::read_i32(buf, offset, same_endianness)".to_owned(),
        ("u32", 4) => "_codec::read_u32(buf, offset, same_endianness)".to_owned(),
        ("f32", 4) => "_codec::read_f32(buf, offset, same_endianness)".to_owned(),
        ("i64", 8) => "_codec::read_i64(buf, offset, same_endianness)".to_owned(),
        ("u64", 8) => "_codec::read_u64(buf, offset, same_endianness)".to_owned(),
        ("f64", 8) => "_codec::read_f64(buf, offset, same_endianness)".to_owned(),
        _ => "_codec::read_i32(buf, offset, same_endianness)".to_owned(),
    };
    (expr, byte_size)
}

fn is_enum_type(name: &str, all_types: &[TypeDef]) -> bool {
    all_types
        .iter()
        .any(|t| matches!(t, TypeDef::Simple(s) if s.name == name))
}

fn is_known_complex_type(name: &str, all_types: &[TypeDef]) -> bool {
    all_types
        .iter()
        .any(|t| matches!(t, TypeDef::Complex(c) if c.name == name))
}

fn is_complex_type(type_ref: &TypeRef, all_types: &[TypeDef]) -> bool {
    match type_ref {
        TypeRef::Named(name) => is_known_complex_type(name, all_types),
        TypeRef::Builtin(_) => false,
    }
}

fn field_base_type(type_ref: &TypeRef, all_types: &[TypeDef], tag: u32) -> (String, String) {
    match type_ref {
        TypeRef::Builtin(p) => {
            let m = map_primitive(*p);
            let prost = format!("{}, tag=\"{}\"", m.prost_tag, tag);
            (m.rust_type.to_owned(), prost)
        }
        TypeRef::Named(name) => {
            if is_enum_type(name, all_types) {
                let prost = format!("enumeration=\"{}\", tag=\"{}\"", name, tag);
                ("i32".to_owned(), prost)
            } else if is_known_complex_type(name, all_types) {
                // Nested message — prost requires Option<T> with `message, tag=...`
                let prost = format!("message, tag=\"{}\"", tag);
                (name.clone(), prost)
            } else {
                // Unresolved type — not found in the loaded XSD set.
                // Falls back to Vec<u8>; re-run spear-gen with the missing
                // XSD file included to get the real type.
                eprintln!("warning: unresolved type '{name}' — falling back to Vec<u8>");
                let prost = format!("bytes, tag=\"{}\"", tag);
                ("Vec<u8>".to_owned(), prost)
            }
        }
    }
}

fn snake_case(s: &str) -> String {
    let mut out = String::new();
    for (i, c) in s.chars().enumerate() {
        if c.is_uppercase() && i > 0 {
            out.push('_');
        }
        out.push(c.to_lowercase().next().unwrap());
    }
    out
}

fn escape_keyword(s: &str) -> String {
    match s {
        "type" | "ref" | "fn" | "mod" | "use" | "move" | "match" | "loop" | "if" | "else"
        | "while" | "for" | "in" | "let" | "mut" | "const" | "static" | "return" | "break"
        | "continue" | "struct" | "enum" | "trait" | "impl" | "where" | "pub" | "super"
        | "self" | "Self" | "crate" | "extern" | "unsafe" | "async" | "await" | "dyn"
        | "abstract" | "become" | "box" | "do" | "final" | "macro" | "override" | "priv"
        | "try" | "typeof" | "unsized" | "virtual" | "yield" => format!("{s}_field"),
        _ => s.to_owned(),
    }
}
